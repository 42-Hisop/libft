#include "libft.h"

void	*ft_memccpy(void *dst, void *src, int c, unsigned int size)
{
	unsigned int	i;

	i = 0;
	while (i < size)
	{
		*((unsigned char *)dst + i) = *((unsigned char *)src + i);
		if (*((unsigned char *)src + i) == (unsigned char)c)
			return (dst + i + 1);
		i++;
	}
	return (0);
}
